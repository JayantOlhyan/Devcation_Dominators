export function getLocalizedIssueCopy(issue: Issue, language: Language) {
  if (language === 'en') {
    return issue;
  }

  const copy = ISSUE_COPY[language][issue.id];

  return {
    ...issue,
    title: copy?.title || issue.title,
    description: copy?.description || issue.description,
    address: copy?.address || issue.address,
    city: getLocalizedCityName(issue.city, language),
    state: getLocalizedStateName(issue.state, language),
  };
}
